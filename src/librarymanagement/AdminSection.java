    public void connect(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/librarydb","root","");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminSection.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AdminSection.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void tableLoad(){
        int a;
        try {
            pst = con.prepareStatement("select * from librarian");
            rs = pst.executeQuery();
            rsd = rs.getMetaData();
            a = rsd.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)tableLibrarian.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector vec = new Vector();
                for(int i=1; i<=a; i++){
                    vec.add(rs.getString("id"));
                    vec.add(rs.getString("name"));
                    vec.add(rs.getString("serviceid"));
                    vec.add(rs.getString("telephone"));
                    vec.add(rs.getString("email"));
                    vec.add(rs.getString("username"));
                    vec.add(rs.getString("password"));
                }
                dtm.addRow(vec);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AdminSection.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
