    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tableLibrarian.getModel();
        int selectIndex = tableLibrarian.getSelectedRow();
        int id = Integer.parseInt(dtm.getValueAt(selectIndex, 0).toString());
        
        try {
            pst = (PreparedStatement) con.prepareStatement("delete from librarian where id=?");
            pst.setInt(1, id);
            int n = pst.executeUpdate();
            
            if(n == 1){
                JOptionPane.showMessageDialog(this, "Librarian deleted successfully.");
                tableLoad();
            }else{
                JOptionPane.showMessageDialog(this, "Error deleting the librarian.");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AddLibrarian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

