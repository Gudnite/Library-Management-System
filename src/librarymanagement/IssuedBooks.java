    public void connect(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/librarydb","root","");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminSection.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AdminSection.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void tableLoad(){
        int a;
        try {
            pst = con.prepareStatement("select * from issued");
            rs = pst.executeQuery();
            rsd = rs.getMetaData();
            a = rsd.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)tableAll.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector vec = new Vector();
                for(int i=1; i<=a; i++){
                    vec.add(rs.getString("bookid"));
                    vec.add(rs.getString("issuedate"));
                    vec.add(rs.getString("returndate"));
                    vec.add(rs.getString("studentid"));
                    vec.add(rs.getString("studentname"));
                    vec.add(rs.getString("telephone"));
                    vec.add(rs.getString("email"));
                }
                dtm.addRow(vec);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AdminSection.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
