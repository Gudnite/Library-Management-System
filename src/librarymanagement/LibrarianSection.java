    private void btnIssuebookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssuebookActionPerformed
        // TODO add your handling code here:
        try {
            pst3 = con.prepareStatement("select * from books where bookid=? and status='Yes'");
            pst3.setString(1, txtBookid.getText());
            rs = pst3.executeQuery();
            
            if(rs.next()){
                
                try {
                    
                    SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
                    String date = sdf.format(dateReturn.getDate());
                     
                    connect();
                    pst1 = con.prepareStatement("UPDATE `books` SET `status` = 'No', `returndate` = '"+date+"' WHERE `books`.`bookid` ='"+txtBookid.getText()+"'");
                    int n = pst1.executeUpdate();

                    if(n == 1){

                        pst2 = con.prepareStatement("insert into issued(bookid,issuedate,returndate,studentid,studentname,telephone,email)values(?,?,?,?,?,?,?)");
                        pst2.setString(1, txtBookid.getText());

                        pst2.setString(2, txtIssue.getText());

                        
                        pst2.setString(3, date);

                        pst2.setString(4, txtStuid.getText());
                        pst2.setString(5, txtStuname.getText());
                        pst2.setString(6, txtStutele.getText());
                        pst2.setString(7, txtStuemail.getText());
                        int m = pst2.executeUpdate();

                        if(m == 1){
                            JOptionPane.showMessageDialog(this, "Book successfully issued.");

                            txtBookid.setText("");
                            dateReturn.setDate(null);
                            txtStuid.setText("");
                            txtStuname.setText("");
                            txtStutele.setText("");
                            txtStuemail.setText("");

                        }else{
                            JOptionPane.showMessageDialog(this, "Error occured issuing this book.");
                        }

                    }else{
                        JOptionPane.showMessageDialog(this, "Error occured issuing this book.");
                    }    

                } catch (SQLException ex) {
                    Logger.getLogger(LibrarianSection.class.getName()).log(Level.SEVERE, null, ex);
                }
                                
            }else{
                JOptionPane.showMessageDialog(this, "Book already issued.");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(LibrarianSection.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }//GEN-LAST:event_btnIssuebookActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            connect();
            pst1 = con.prepareStatement("insert into books(title,author,category,publisher,status,returndate)values(?,?,?,?,'Yes','---')");
            
            pst1.setString(1, txtTitle.getText());
            pst1.setString(2, txtAuthor.getText());
            pst1.setString(3, (String) comboCategory.getSelectedItem());
            pst1.setString(4, txtPublisher.getText());
            
            int n = pst1.executeUpdate();
            
            if(n == 1){
                JOptionPane.showMessageDialog(this, "New book added successfully.");
                txtTitle.setText("");
                txtAuthor.setText("");
                comboCategory.setSelectedIndex(0);
                txtPublisher.setText("");
            }else{
                JOptionPane.showMessageDialog(this, "Error adding new book.");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(LibrarianSection.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

