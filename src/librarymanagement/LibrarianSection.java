    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        // TODO add your handling code here:
        try {
            connect();
            pst1 = con.prepareStatement("delete from issued where bookid=? and studentid=?");
            pst1.setString(1, txtRebookid.getText());
            pst1.setString(2, txtRestuid.getText());
            
            int k = pst1.executeUpdate();
            
            if(k == 1){
                pst2 = con.prepareStatement("UPDATE `books` SET `status` = 'Yes', `returndate` = '---' WHERE `books`.`bookid` ='"+txtRebookid.getText()+"'");
                int n =pst2.executeUpdate();
                if(n == 1){
                JOptionPane.showMessageDialog(this, "Book successfully returned.");
                txtRebookid.setText("");
                txtRestuid.setText("");
                    
                }
            }else {
                pst3 = con.prepareStatement("select * from issued where bookid=?");
                pst3.setString(1, txtRebookid.getText());
                rs = pst3.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Book ID and Student ID does not match.");
                }else{
                    JOptionPane.showMessageDialog(this, "Book already returned.");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(LibrarianSection.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnReturnActionPerformed

